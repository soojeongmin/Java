package chap99_Homework.homework12;

public class AppendAndReverse {
	public void appendStringBuilder(StringBuilder sb1, AppendStringBuilder appendStringBuilder) {
		appendStringBuilder.appendSb(sb1);
	}
	
	public void reverseStringBuilder(StringBuilder sb1, ReverseStringBuilder reverseStringBuilder) {
		reverseStringBuilder.reverseSb(sb1);
	}
	
	public void combineStringBuilder(StringBuilder sb1, StringBuilder sb2, StringBuilderUtils stringBuilderUtils) {
		stringBuilderUtils.combineStrBuilder(sb1, sb2);
	}
}
